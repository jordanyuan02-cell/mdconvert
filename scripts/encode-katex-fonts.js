/**
 * Encode KaTeX fonts as base64 data URLs directly in CSS.
 *
 * This completely bypasses Tauri v2's asset protocol MIME type issues
 * for .woff2 files. By embedding fonts as data: URLs in the CSS,
 * the fonts are guaranteed to load regardless of how the server
 * serves files (HTTP, Tauri custom protocol, etc.).
 *
 * Run as part of `pnpm build` (before `vite build`).
 * Generates: src/styles/katex-base64.css
 */

import { readFileSync, writeFileSync, existsSync, readdirSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));

const KATEX_FONT_DIR = resolve(__dirname, '../node_modules/katex/dist/fonts');
const OUTPUT_CSS = resolve(__dirname, '../src/styles/katex-base64.css');

/**
 * Map KaTeX font variant names to CSS font-weight and font-style.
 */
function variantToCSS(variant) {
  switch (variant) {
    case 'Regular':
      return { weight: '400', style: 'normal' };
    case 'Bold':
      return { weight: '700', style: 'normal' };
    case 'Italic':
      return { weight: '400', style: 'italic' };
    case 'BoldItalic':
      return { weight: '700', style: 'italic' };
    default:
      return { weight: '400', style: 'normal' };
  }
}

/**
 * Parse a KaTeX font filename (without extension) into family and variant.
 * Format: "{family}-{variant}"
 * Examples:
 *   "KaTeX_Main-Regular"    → family="KaTeX_Main",  variant="Regular"
 *   "KaTeX_Main-BoldItalic"  → family="KaTeX_Main",  variant="BoldItalic"
 *   "KaTeX_Size2-Regular"    → family="KaTeX_Size2", variant="Regular"
 */
function parseFontName(filename) {
  // Split on last hyphen
  const lastHyphen = filename.lastIndexOf('-');
  if (lastHyphen === -1) {
    return { family: filename, variant: 'Regular' };
  }
  return {
    family: filename.substring(0, lastHyphen),
    variant: filename.substring(lastHyphen + 1),
  };
}

function main() {
  if (!existsSync(KATEX_FONT_DIR)) {
    console.error(`KaTeX font directory not found: ${KATEX_FONT_DIR}`);
    console.error('Make sure KaTeX is installed: pnpm install');
    process.exit(1);
  }

  // Read all .woff2 files
  const files = readdirSync(KATEX_FONT_DIR).filter(
    (f) => f.endsWith('.woff2')
  );

  if (files.length === 0) {
    console.error('No .woff2 font files found in KaTeX font directory.');
    process.exit(1);
  }

  console.log(`Found ${files.length} KaTeX .woff2 font files.`);

  let css = `/* ============================================================
   KaTeX Base64 Font Embedding
   Auto-generated by scripts/encode-katex-fonts.js
   DO NOT EDIT MANUALLY — regenerate by running 'pnpm run encode-fonts'
   
   This embeds all KaTeX .woff2 fonts as base64 data URLs to bypass
   Tauri v2's asset protocol MIME type issues in production builds.
   ============================================================ */

`;

  for (const file of files.sort()) {
    const fontPath = resolve(KATEX_FONT_DIR, file);
    const buffer = readFileSync(fontPath);
    const base64 = buffer.toString('base64');
    const sizeKB = (buffer.length / 1024).toFixed(1);

    // Parse font name without extension
    const nameWithoutExt = file.replace(/\.woff2$/, '');
    const { family, variant } = parseFontName(nameWithoutExt);
    const { weight, style } = variantToCSS(variant);

    css += `@font-face {
  font-family: '${family}';
  font-style: ${style};
  font-weight: ${weight};
  font-display: swap;
  src: url(data:font/woff2;base64,${base64}) format('woff2');
}

`;
    console.log(`  Encoded: ${file} (${sizeKB} KB → base64)`);
  }

  writeFileSync(OUTPUT_CSS, css, 'utf-8');
  console.log(`\n✅ Generated: ${OUTPUT_CSS}`);
  console.log(`   Total CSS size: ${(css.length / 1024).toFixed(1)} KB`);
}

try {
  main();
} catch (err) {
  console.error('Failed to encode KaTeX fonts:', err);
  process.exit(1);
}
